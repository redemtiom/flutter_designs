      ),
    );
  }
}

class _HeaderGradientPainter extends CustomPainter {
  @override
  void paint(Canvas canvas, Size size) {
    final Rect rect = Rect.fromCircle(
      center: const Offset(0.0, 55.0),
      radius: 180,
    );

    const Gradient gradient = LinearGradient(
        begin: Alignment.topCenter,
        end: Alignment.bottomCenter,
        colors: <Color>[
          Color(0xff6D05E8),
          Color(0xffC012FF),
          Color(0xff8D05FA),
        ],
        stops: [
          0.2,
          0.5,
          1.0
        ]);

    final paint = Paint()..shader = gradient.createShader(rect);
    paint.color = const Color(0xff615AAB);
    paint.style = PaintingStyle.fill;
    paint.strokeWidth = 20.0;

    final path = Path();

    path.lineTo(0, size.height * 0.25);
    path.quadraticBezierTo(size.width * 0.25, size.height * 0.35,
        size.width * 0.50, size.height * 0.25);
    path.quadraticBezierTo(
        size.width * 0.75, size.height * 0.15, size.width, size.height * 0.25);
    path.lineTo(size.width, 0);

    canvas.drawPath(path, paint);
  }

  @override
  bool shouldRepaint(covariant CustomPainter oldDelegate) {
    return true;
  }
}

class IconHeader extends StatelessWidget {
  const IconHeader(
      {Key? key,
      required this.icon,
      required this.title,
      required this.subtitle,
      this.color1 = const Color(0xff526BF6),
      this.color2 = const Color(0xff67ACF2)})
      : super(key: key);

  final IconData icon;
  final String title;
  final String subtitle;
  final Color color1;
  final Color color2;

  @override
  Widget build(BuildContext context) {
    final Color colorBlanco = Colors.white.withOpacity(0.7);

    return Stack(
      children: [
        _IconHeaderBackground(color1: color1, color2: color2,),
        Positioned(
          top: -50.0,
          left: -70.0,
          child: FaIcon(FontAwesomeIcons.plus,
              size: 250.0, color: Colors.white.withOpacity(0.2)),
        ),
        Column(
          children: [
            const SizedBox(
              height: 80.0,
              width: double.infinity,
            ),
            Text(
              title,
              style: TextStyle(fontSize: 20.0, color: colorBlanco),
            ),
            const SizedBox(
              height: 20.0,
            ),
            Text(
              subtitle,
              style: TextStyle(
                  fontSize: 25.0,
                  fontWeight: FontWeight.bold,
                  color: colorBlanco),
            ),
            const SizedBox(
              height: 20.0,
            ),
            FaIcon(icon, size: 80.0, color: Colors.white)
          ],
        )
      ],
    );
  }
}

class _IconHeaderBackground extends StatelessWidget {
  const _IconHeaderBackground({
    Key? key,
    required this.color1,
    required this.color2
  }) : super(key: key);

  final Color color1;
  final Color color2;

  @override
  Widget build(BuildContext context) {
    return Container(
      width: double.infinity,
      height: 300.0,
      decoration: BoxDecoration(
          color: Colors.red,
          borderRadius: BorderRadius.only(bottomLeft: Radius.circular(80.0)),
          gradient: LinearGradient(
              begin: Alignment.topCenter,
              end: Alignment.bottomCenter,
              colors: [color1, color2])),
    );
  }
}
